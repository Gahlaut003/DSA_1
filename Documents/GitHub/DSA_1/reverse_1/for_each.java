class REVERSE_1 {
    public static void main(String[] args) {
        forEach();

    }

    public static void forEach() {
        int arr1[] = { 123, 456, 789, 987, 654, 4, 3, 21 };
        for (int num : arr1) {
            System.out.println(num);
        }

        String[] arr = new String[] { "abhi", "akg", "ashjdgkahsjdf" };
        for (String i : arr) {
            System.out.println(i);
        }

        char[] charArray = new char[] { 'e', 'e', 'e' };
        for (char j : charArray) {
            System.out.println(j);
        }

    }

}
